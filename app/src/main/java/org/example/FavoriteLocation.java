package org.example;

public class FavoriteLocation {
    private String name;
    private String path;

    public FavoriteLocation() {
    }

    public String getName() {
        return name;
    }

    public String getPath() {
        return path;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPath(String path) {
        this.path = path;
    }
}